// Program to print 5 times "Marvellous" on screen.

public class Program2 {

    public static void Display() {
        int iCnt = 0;
        for (iCnt = 1; iCnt <= 5; iCnt++) {
            System.out.println("Marvellous");
        }
    }

    public static void main(String[] args) {
        Display();

    }

}
